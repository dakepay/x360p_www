            $employee = Employee::get(['uid' => $this->getAttr('uid')]);
            if(empty($employee)) return [];
            //if(in_array(10, $employee['rids'])) return $branchs;    //系统管理员返回所有校区
            return $employee->getAttr('branches');
        }
	}


