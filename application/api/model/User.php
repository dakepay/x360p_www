                        break;
                    } else {
                        $url_list[] = $url;
                    }
                }
                $url = array_shift($url_list);
            }
            Cache::set($cache_key, 0, 300);
            return $url;
        } catch (\Exception $exception) {
            return $this->user_error($exception->getMessage());
        }
